const CONFIG = {
  minDelay: 3000,
  maxDelay: 7000,
  minCharDelay: 50,   // ✨ delay mínimo entre letras
  maxCharDelay: 150,  // ✨ delay máximo entre letras
  waitAfterWrite: 1000,
  reintentos: 3,
};
// ─────────────────────────────────────────────────────────────

let totalEnviados = 0;
let indiceActual = 0;
let corriendo = true;

// ─── UTILIDADES ──────────────────────────────────────────────
function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

function randomDelay(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

// ─── COLA INTELIGENTE ─────────────────────────────────────────
class ColaComentarios {
  constructor(comentarios) {
    this.originales = [...comentarios];
    this.cola = [];
    this.ultimoUsado = null;
  }

  mezclar(arr) {
    const a = [...arr];
    for (let i = a.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
  }

  siguiente() {
    if (this.cola.length === 0) {
      this.cola = this.mezclar(this.originales);

      // Evita repetir el último comentario usado
      if (this.cola[0] === this.ultimoUsado && this.cola.length > 1) {
        [this.cola[0], this.cola[1]] = [this.cola[1], this.cola[0]];
      }
    }

    this.ultimoUsado = this.cola.shift();
    return this.ultimoUsado;
  }
}

const cola = new ColaComentarios(comentarios);

// ─── ESCRITURA REALISTA ───────────────────────────────────────
async function escribirComentario(textarea, texto) {
  textarea.click();
  textarea.focus();
  await sleep(600);

  // Limpiar campo
  textarea.select();
  document.execCommand('selectAll', false, null);
  document.execCommand('delete', false, null);
  await sleep(400);

  // Escribir letra por letra simulando teclado real
  for (const char of texto) {
    // Simular tecla presionada
    textarea.dispatchEvent(new KeyboardEvent('keydown', {
      key: char,
      bubbles: true,
      cancelable: true
    }));

    // Insertar carácter
    document.execCommand('insertText', false, char);

    // InputEvent real como lo genera el navegador
    textarea.dispatchEvent(new InputEvent('input', {
      inputType: 'insertText',
      data: char,
      bubbles: true,
      cancelable: true
    }));

    // Simular tecla soltada
    textarea.dispatchEvent(new KeyboardEvent('keyup', {
      key: char,
      bubbles: true
    }));

    // Delay humano entre letras
    await sleep(randomDelay(CONFIG.minCharDelay, CONFIG.maxCharDelay));
  }

  // Evento change al terminar de escribir
  textarea.dispatchEvent(new Event('change', { bubbles: true }));
  await sleep(CONFIG.waitAfterWrite);
}

// ─── ESPERAR BOTÓN ────────────────────────────────────────────
async function esperarBotonActivo(maxIntentos = 40) {
  for (let i = 0; i < maxIntentos; i++) {
    const botones = document.querySelectorAll('div[role="button"][tabindex="0"]');
    for (const boton of botones) {
      if (
        boton.textContent.trim() === 'Publicar' &&
        !boton.hasAttribute('aria-disabled')
      ) {
        return boton;
      }
    }
    await sleep(300);
  }
  return null;
}

// ─── PUBLICAR ─────────────────────────────────────────────────
async function publicarComentario(texto) {
  for (let intento = 1; intento <= CONFIG.reintentos; intento++) {
    if (intento > 1) {
      console.log(`🔄 Reintento ${intento}/${CONFIG.reintentos}...`);
      await sleep(1000);
    }

    const textarea = document.querySelector('textarea[aria-label="Agrega un comentario..."]');
    if (!textarea) {
      console.error('❌ No se encontró el textarea');
      continue;
    }

    await escribirComentario(textarea, texto);

    const valorActual = textarea.value;
    if (!valorActual || valorActual.trim() === '') {
      console.error('❌ El textarea quedó vacío, reintentando...');
      continue;
    }

    const boton = await esperarBotonActivo();
    if (!boton) {
      console.error('❌ Botón no se activó, reintentando...');
      continue;
    }

    boton.click();
    totalEnviados++;
    console.log(`🚀 Publicado! Total enviados: ${totalEnviados}`);
    await sleep(1500);
    return true;
  }

  console.warn(`⚠️ No se pudo publicar tras ${CONFIG.reintentos} intentos, saltando...`);
  return false;
}

// ─── LOOP PRINCIPAL ───────────────────────────────────────────
async function iniciarBot() {
  console.log('🤖 Bot mejorado iniciado. Para detener escribe: corriendo = false');

  while (corriendo) {
    const texto = cola.siguiente(); // ✨ Cola inteligente
    indiceActual++;

    console.log(`\n📝 Comentario #${indiceActual} → "${texto}"`);

    await publicarComentario(texto);

    if (!corriendo) break;

    const espera = randomDelay(CONFIG.minDelay, CONFIG.maxDelay);
    console.log(`⏳ Esperando ${(espera / 1000).toFixed(1)}s...`);
    await sleep(espera);
  }

  console.log(`\n🛑 Bot detenido. Total enviados: ${totalEnviados}`);
}

